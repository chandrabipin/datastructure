package com.bchandra.datastructure;

public class OO8BinaryTreeNode {
	private int value;
	private int height;
	private OO8BinaryTreeNode leftNode;
	private OO8BinaryTreeNode rightNode;
	/**
	 * @return the value
	 */
	public int getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(int value) {
		this.value = value;
	}
	/**
	 * @return the height
	 */
	public int getHeight() {
		return height;
	}
	/**
	 * @param height the height to set
	 */
	public void setHeight(int height) {
		this.height = height;
	}
	/**
	 * @return the leftNode
	 */
	public OO8BinaryTreeNode getLeftNode() {
		return leftNode;
	}
	/**
	 * @param leftNode the leftNode to set
	 */
	public void setLeftNode(OO8BinaryTreeNode leftNode) {
		this.leftNode = leftNode;
	}
	/**
	 * @return the rightNode
	 */
	public OO8BinaryTreeNode getRightNode() {
		return rightNode;
	}
	/**
	 * @param rightNode the rightNode to set
	 */
	public void setRightNode(OO8BinaryTreeNode rightNode) {
		this.rightNode = rightNode;
	}
	

}
